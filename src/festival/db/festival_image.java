package festival.db;

public class festival_image {
	private int festival_igm_no;
	private String festival_img_name;
	private int festival_img_type;
	private int festival_no;
	
	
	
	public int getFestival_igm_no() {
		return festival_igm_no;
	}
	public void setFestival_igm_no(int festival_igm_no) {
		this.festival_igm_no = festival_igm_no;
	}
	public String getFestival_img_name() {
		return festival_img_name;
	}
	public void setFestival_img_name(String festival_img_name) {
		this.festival_img_name = festival_img_name;
	}
	public int getFestival_img_type() {
		return festival_img_type;
	}
	public void setFestival_img_type(int festival_img_type) {
		this.festival_img_type = festival_img_type;
	}
	public int getFestival_no() {
		return festival_no;
	}
	public void setFestival_no(int festival_no) {
		this.festival_no = festival_no;
	}
	
	
}
