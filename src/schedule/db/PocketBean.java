package schedule.db;

public class PocketBean {
	private int user_no;
	private int pocket_no;
	private int travel_no;
	private int festival_no;
	public int getUser_no() {
		return user_no;
	}
	public void setUser_no(int user_no) {
		this.user_no = user_no;
	}
	public int getPocket_no() {
		return pocket_no;
	}
	public void setPocket_no(int pocket_no) {
		this.pocket_no = pocket_no;
	}
	public int getTravel_no() {
		return travel_no;
	}
	public void setTravel_no(int travel_no) {
		this.travel_no = travel_no;
	}
	public int getFestival_no() {
		return festival_no;
	}
	public void setFestival_no(int festival_no) {
		this.festival_no = festival_no;
	}
	
}
